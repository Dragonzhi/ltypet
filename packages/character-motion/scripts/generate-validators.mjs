import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

const packageRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const schemaRoot = path.join(packageRoot, "src", "schema");
const outputRoot = path.join(schemaRoot, "generated");

function readJson(name) {
  return JSON.parse(readFileSync(path.join(schemaRoot, name), "utf8"));
}

function generate(schema, outputName) {
  const ajv = new Ajv2020({
    strict: true,
    allErrors: true,
    validateSchema: true,
    code: { source: true, esm: true, optimize: true, lines: true },
  });
  const validate = ajv.compile(schema);
  let source = standaloneCode(ajv, validate)
    .replace(/^"use strict";/u, "")
    .trimStart();
  const runtimeImports = [];
  source = source.replace(
    /const\s+([A-Za-z_$][\w$]*)\s*=\s*require\("([^"]+)"\)\.default;/gu,
    (_statement, binding, moduleName) => {
      runtimeImports.push(`import ${binding} from "${moduleName}.js";`);
      return "";
    },
  );
  if (runtimeImports.length > 0) source = `${runtimeImports.join("\n")}\n${source}`;
  const banner = [
    "// Generated by scripts/generate-validators.mjs. Do not edit by hand.",
    "// This removes Ajv runtime code generation so strict CSP can remain enabled.",
    "// @ts-nocheck",
    "",
  ].join("\n");
  writeFileSync(path.join(outputRoot, outputName), `${banner}${source}\n`, "utf8");
}

mkdirSync(outputRoot, { recursive: true });
generate(readJson("rig.schema.json"), "validateRig.generated.ts");
generate(readJson("motions.schema.json"), "validateMotions.generated.ts");
console.log("Generated CSP-safe standalone validators for rig v1 and motions v1.");
